// Helper function to filter cities based on search term
export function filterCities(searchTerm: string, maxResults: number = 10): USCity[] {
  if (!searchTerm || searchTerm.length < 2) return []
  
  const normalizedSearch = searchTerm.toLowerCase().trim()
  
  return usCities
    .filter(city => 
      city.name.toLowerCase().includes(normalizedSearch) ||
      city.state.toLowerCase().includes(normalizedSearch)
    )
    .sort((a, b) => {
      // Prioritize exact matches and matches at the beginning
      const aNameLower = a.name.toLowerCase()
      const bNameLower = b.name.toLowerCase()
      
      const aStartsWith = aNameLower.startsWith(normalizedSearch)
      const bStartsWith = bNameLower.startsWith(normalizedSearch)
      
      if (aStartsWith && !bStartsWith) return -1
      if (!aStartsWith && bStartsWith) return 1
      
      // Then sort by population (larger cities first)
      return b.population - a.population
    })
    .slice(0, maxResults)
} 